package chat.view;

import java.awt.Color;
import java.awt.event.ActionEvent;
import java.awt.event.ActionListener;


import javax.swing.JButton;
import javax.swing.JLabel;
import javax.swing.JPanel;
import javax.swing.JTextArea;
import javax.swing.JTextField;
import javax.swing.SpringLayout;

import javax.swing.*;

import chat.controller.ChatbotController;

/**
 * Creates the inside of the GUI window.
 * @author jbur0473
 * @version 1.3 Added tweet button
 */
public class ChatPanel extends JPanel
{
	//declares all variables
	private ChatbotController baseController;
	private JTextArea chatArea;
	private JTextField typingField;
	private SpringLayout baseLayout;
	private JButton submitButton;
	private JLabel promptLabel;
	private JButton tweetButton;
	private JButton analyzeTwitterButton;
	private JButton loadButton;
	private JButton saveButton;


	private JScrollPane textPane;

	
	/**
	 * Initializes variables, calls all methods.
	 * @param baseController
	 */
	public ChatPanel(ChatbotController baseController)
	{
		this.baseController = baseController;
		
		baseLayout = new SpringLayout();
		chatArea = new JTextArea(10,30);
		typingField = new JTextField(30);
		submitButton = new JButton("Submit");
		promptLabel = new JLabel("Chat me up like one of your french bots.");
		tweetButton = new JButton("Tweet");
		baseLayout.putConstraint(SpringLayout.SOUTH, tweetButton, -6, SpringLayout.NORTH, typingField);
		analyzeTwitterButton = new JButton("analyze");
		baseLayout.putConstraint(SpringLayout.EAST, analyzeTwitterButton, 0, SpringLayout.EAST, this);
		saveButton = new JButton("save");
		baseLayout.putConstraint(SpringLayout.SOUTH, saveButton, -6, SpringLayout.NORTH, tweetButton);
		loadButton = new JButton("load");
		baseLayout.putConstraint(SpringLayout.NORTH, loadButton, 35, SpringLayout.NORTH, this);
		baseLayout.putConstraint(SpringLayout.EAST, loadButton, 0, SpringLayout.EAST, this);


		

		setupPane();
		setupPanel();
		setupLayout();
		setupListeners();
	}
	
	//adds required variables to the pane.
	private void setupPane()
	{
		chatArea.setLineWrap(true);
		chatArea.setWrapStyleWord(true);
		chatArea.setEnabled(false);
		chatArea.setEditable(false);
		chatArea.setBackground(Color.GRAY);
		textPane = new JScrollPane(chatArea);
		baseLayout.putConstraint(SpringLayout.WEST, saveButton, 0, SpringLayout.WEST, textPane);
		baseLayout.putConstraint(SpringLayout.WEST, tweetButton, 0, SpringLayout.WEST, textPane);
		textPane.setHorizontalScrollBarPolicy(ScrollPaneConstants.HORIZONTAL_SCROLLBAR_NEVER);
		textPane.setVerticalScrollBarPolicy(ScrollPaneConstants.VERTICAL_SCROLLBAR_AS_NEEDED);
	}
	
	/**
	 * Adds all elements of window, sets values and background color.
	 */
	private void setupPanel()
	{
		this.setLayout(baseLayout);
		this.setBackground(Color.GREEN);
		this.add(textPane);
		this.add(typingField);
		this.add(submitButton);
		this.add(tweetButton);
		this.add(saveButton);
		this.add(analyzeTwitterButton);
		this.add(loadButton);
		this.add(promptLabel);
		typingField.setToolTipText("Type here for the chatbot");
		chatArea.setEnabled(false);
	}

	/**
	 * Dumping ground for all code generated by window design sector.
	 */
	private void setupLayout()
	{
		baseLayout.putConstraint(SpringLayout.NORTH, chatArea, 10, SpringLayout.NORTH, this);
		baseLayout.putConstraint(SpringLayout.WEST, chatArea, 10, SpringLayout.WEST, this);
		baseLayout.putConstraint(SpringLayout.WEST, typingField, 0, SpringLayout.WEST, this);
		baseLayout.putConstraint(SpringLayout.SOUTH, typingField, -2, SpringLayout.SOUTH, this);
		baseLayout.putConstraint(SpringLayout.WEST, submitButton, 6, SpringLayout.EAST, typingField);
		baseLayout.putConstraint(SpringLayout.SOUTH, submitButton, 0, SpringLayout.SOUTH, this);
		baseLayout.putConstraint(SpringLayout.EAST, submitButton, 0, SpringLayout.EAST, this);
		baseLayout.putConstraint(SpringLayout.WEST, promptLabel, 171, SpringLayout.WEST, this);
		baseLayout.putConstraint(SpringLayout.SOUTH, promptLabel, -33, SpringLayout.NORTH, typingField);
		
	}
	
	/**
	 * Programs the submit button to actually submit the text when clicked.
	 */
	private void setupListeners()
	{
		submitButton.addActionListener(new ActionListener()
		{
			public void actionPerformed(ActionEvent click)
			{
				String userText = typingField.getText();					//Grab user typed answer
				chatArea.append("\nUser: " + userText);						//display typed answer
				typingField.setText("");									//send the text to chatbot
				String response = baseController.userToChatbot(userText);	//chatbot will process
				chatArea.append("\nChatbot: " + response);					//display the response
			}
			
		});
		
		//sends a base text of "no text to send"
		tweetButton.addActionListener(new ActionListener()
		{
			public void actionPerformed(ActionEvent click)
			{
				baseController.sendTweet("no text to send");
			}
		});
		
		//returns results for a twitter query
		analyzeTwitterButton.addActionListener(new ActionListener()
		{
			public void actionPerformed(ActionEvent click)
			{
				String user = typingField.getText();
				String results = baseController.analyze(user);
				chatArea.setText(results);
			}
		});
		
		//saves you conversation so far
		saveButton.addActionListener(new ActionListener()
		{
			public void actionPerformed(ActionEvent click)
			{
				String user = typingField.getText();
				String results = baseController.analyze(user);
				chatArea.setText(results);
			}
		});
		
		//loads a conversation from earlier.
		loadButton.addActionListener(new ActionListener()
		{
			 public void actionPerformed(ActionEvent click)
			 {
				 String saved = chatArea.getText();
				 typingField.setText(saved);
			 }
		});
	}
	
	public ChatbotController getBaseController()
	{
		return baseController;
	}

	public void setBaseController(ChatbotController baseController)
	{
		this.baseController = baseController;
	}

	public JTextArea getChatArea()
	{
		return chatArea;
	}

	public void setChatArea(JTextArea chatArea)
	{
		this.chatArea = chatArea;
	}

	public JTextField getTextField()
	{
		return typingField;
	}

	public void setTypingField(JTextField typingField)
	{
		this.typingField = typingField;
	}

	public SpringLayout getBaseLayout()
	{
		return baseLayout;
	}

	public void setBaseLayout(SpringLayout baseLayout)
	{
		this.baseLayout = baseLayout;
	}

	public JButton getSubmitButton()
	{
		return submitButton;
	}

	public void setSubmitButton(JButton submitButton)
	{
		this.submitButton = submitButton;
	}

	public JLabel getPromptLabel()
	{
		return promptLabel;
	}

	public void setPromptLabel(JLabel promptLabel)
	{
		this.promptLabel = promptLabel;
	}

}
